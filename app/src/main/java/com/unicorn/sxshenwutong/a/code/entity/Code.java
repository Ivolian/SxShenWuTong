package com.unicorn.sxshenwutong.a.code.entity;

public class Code {

    private String dm;
    private String dmms;

    public Code(String dm, String dmms) {
        this.dm = dm;
        this.dmms = dmms;
    }

    public String getDm() {
        return dm;
    }

    public void setDm(String dm) {
        this.dm = dm;
    }

    public String getDmms() {
        return dmms;
    }

    public void setDmms(String dmms) {
        this.dmms = dmms;
    }

}
